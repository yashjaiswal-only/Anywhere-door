function updateResult(query) {
    let resultList = document.querySelector(".tours");
    resultList.innerHTML = "";

    locations.map(function(algo){
        query.split(" ").map(function (word){
            if(algo.name.toLowerCase().indexOf(word.toLowerCase()) != -1){
                resultList.innerHTML += `<div class="view">
				<img src="${algo.image}" >
				<div class="mask">
					<h2>${algo.name}</h2>
					<p>${algo.info}</p>
					<a href="https://en.wikipedia.org/wiki/${algo.name}" target="_blank" class="info">READ MORE</a>
				</div>
			</div>`;
            }
        })
    })
}
    
